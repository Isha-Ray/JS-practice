// Write a program to swap two numbers using variables

let a = 5;
let b = 10;

console.log("Before swapping: ");
console.log("a =", a); // 5
console.log("b =", b); // 10

let temp = a; // temp hota h temporary variable 
a = b;
b = temp;

console.log("After swapping:");
console.log("a =", a); // 10
console.log("b =", b); // 5

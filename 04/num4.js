/*  Write a grading system:
   If marks > 90: A, > 75: B, > 50: C, else Fail. */

let marks = 51;

if(marks > 90){
    console.log("A")
} else if(marks > 75){
    console.log("B")
} else if(marks > 50){
    console.log("C")
} else {
    console.log("Fail")
}


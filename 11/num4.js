// Use .forEach() to print "Value: X" for each element in [10, 20, 30].

const numbers = [10, 20, 30];

numbers.forEach((value) => {
  console.log("Value:", value);
});

/* Output ---------
Value: 10
Value: 20
Value: 30
*/

/*
.forEach() goes through each element in the array.
The value is printed with the text "Value:".
*/
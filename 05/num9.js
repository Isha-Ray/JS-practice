// Create a logic for traffic light (Red: Stop, Yellow: Wait, Green: Go).


let light = "Red"; 

(light === "Red") && console.log("Stop");
(light === "Yellow") && console.log("Wait");
(light === "Green") && console.log("Go");
(light !== "Red" && light !== "Yellow" && light !== "Green") && console.log("Invalid color");

// Output ----- Stop



// Use assignment operators (+=, -=, etc.) to update a score.

let score = 50;

score += 10;  // score = score + 10 → 60
console.log("After += 10: " + score);

score -= 5;   // score = score - 5 → 55
console.log("After -= 5: " + score);

score *= 2;   // score = score * 2 → 110
console.log("After *= 2: " + score);

score /= 5;   // score = score / 5 → 22
console.log("After /= 5: " + score);

score %= 6;   // score = score % 6 → 4
console.log("After %= 6: " + score);


// Output ------------
/*  After += 10: 60
    After -= 5: 55
    After *= 2: 110
    After /= 5: 22
    After %= 6: 4       */


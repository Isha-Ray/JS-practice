// Use comparison operator to compare two variables and print results.

let a = 10;
let b = 20;

console.log("a == b:", a == b);     // Output ----- false (values not equal)
console.log("a != b:", a != b);     // Output ----- true  (values are not equal)
console.log("a > b:", a > b);       // Output ----- false
console.log("a < b:", a < b);       // Output ----- true 
console.log("a >= b:", a >= b);     // Output ----- false
console.log("a <= b:", a <= b);     // Output ----- true


/* Comparison Operator (==, !=, >, <, >=, <=) use karke kisi bhi 2 variable ko compare kar sakte h.

    == : equal to
    != : not equal to
    >  : greater than
    <  : less than
    >= : greater than or equal to
    <= : less than or equal to    */